package com.ent.domain;

public class AdditionalProdCredits
{
	
	/** The legal counsel. */
	private String legalCounsel;
	
	/** The accountant. */
	private String accountant;
	
	/** The insurance broker. */
	private String insuranceBroker;
	
	/** The system administrator. */
	private String systemAdministrator;

	public String getLegalCounsel()
	{
		return legalCounsel;
	}

	public void setLegalCounsel(String legalCounsel)
	{
		this.legalCounsel = legalCounsel;
	}

	public String getAccountant()
	{
		return accountant;
	}

	public void setAccountant(String accountant)
	{
		this.accountant = accountant;
	}

	public String getInsuranceBroker()
	{
		return insuranceBroker;
	}

	public void setInsuranceBroker(String insuranceBroker)
	{
		this.insuranceBroker = insuranceBroker;
	}

	public String getSystemAdministrator()
	{
		return systemAdministrator;
	}

	public void setSystemAdministrator(String systemAdministrator)
	{
		this.systemAdministrator = systemAdministrator;
	}
}
